            var ffMpegVersionCommmand = "ffmpeg -version";
            var ytDlpVersionCommand = "yt-dlp --version";

            var ffmepegVersion = RunUnixCommand(ffMpegVersionCommmand);
            var ytDlpVersion = RunUnixCommand(ytDlpVersionCommand);

            return new string[] { ffmepegVersion, ytDlpVersion };
        }

        private string RunUnixCommand(string command)
        {
            var process = new Process();
            process.StartInfo.FileName = "/bin/bash";
            process.StartInfo.Arguments = $"-c \"{command}\"";
            process.StartInfo.UseShellExecute = false;
            process.StartInfo.RedirectStandardOutput = true;
            process.StartInfo.RedirectStandardError = true;

            process.Start();

            var output = process.StandardOutput.ReadToEnd();
            var error = process.StandardError.ReadToEnd();

            return output;
        }

